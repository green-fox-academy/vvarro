public class TwoNumbers {
  public static void main(String[] args) {
    int firstNumber = 13;
    int secondNumber = 22;
    double add = firstNumber + secondNumber;
    double substr = firstNumber - secondNumber;
    double multiply = secondNumber * firstNumber;
    double divide = secondNumber / firstNumber;
    double integ = secondNumber / firstNumber;
    double reminder = secondNumber % firstNumber;
    System.out.println(add);
    System.out.println(substr);
    System.out.println(multiply);
    System.out.println(divide);
    System.out.println(integ);
    System.out.println(reminder);
  }
}
